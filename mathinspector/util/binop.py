def Add(a,b):
	"""
	a + b : a plus b
	"""
	return a + b

def Sub(a,b):
	"""
	a - b : a minus b
	"""
	return a - b

def Mult(a,b):
	"""
	a * b : a times b
	"""
	return a * b

def Div(a,b):
	"""
	a / b : a divided by b
	"""
	return a / b

def FloorDiv(a,b):
	"""
	a // b : the largest integer less than a divided by b
	"""
	return a // b

def Mod(a,b):
	"""
	a % b : a modulo b
	"""
	return a % b

def Pow(a,b):
	"""
	a ** b : a to the power b
	"""
	return a ** b

def LShift(a,b):
	"""
	a << b : left shift bitwise operator
	"""
	return a << b

def RShift(a,b):
	"""
	a >> b : right shift bitwise operator
	"""
	return a >> b

def BitOr(a,b):
	"""
	a | b : bitwise or operator
	"""
	return a | b

def BitXor(a,b):
	"""
	a ^ b : bitwise xor operator
	"""
	return a ^ b

def BitAnd(a,b):
	"""
	a & b : bitwise and operator
	"""
	return a & b